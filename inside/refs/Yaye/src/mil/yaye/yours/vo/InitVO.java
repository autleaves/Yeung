package mil.yaye.yours.vo;

public class InitVO {
	
	private String key;
	private Object value;
	
	public InitVO(){
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public Object getValue() {
		return value;
	}

	public void setValue(Object value) {
		this.value = value;
	}
	
	
}
